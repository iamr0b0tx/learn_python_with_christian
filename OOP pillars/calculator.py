class Calculator:
    ''' encapsulate the details of the calculator data and implementations '''
    
    def __init__(self):
        pass

    def add(self, num1, num2):
        return num1+num2

    def subtract(self, num1, num2):
        return num1-num2

    def multiply(self, num1, num2):
        return num1 * num2

    def divide(self, num1, num2):
        return num1/num2

